class Solution(object):
    def fourSumCount(self, A, B, C, D):
        """
        :type A: List[int]
        :type B: List[int]
        :type C: List[int]
        :type D: List[int]
        :rtype: int
        """
        from collections import Counter
        sumab = Counter(x + y for x in A for y in B)
        return sum(sumab[-c-d] for c in C for d in D)



